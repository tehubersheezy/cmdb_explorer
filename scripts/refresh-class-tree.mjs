#!/usr/bin/env node
/*
 * Refresh src/client/data/classTree.ts from a live ServiceNow instance.
 *
 * The CMDB class HIERARCHY changes rarely, so the explorer ships it statically
 * (the live sys_db_object `super_class.name` dot-walk takes ~30s and 504s behind
 * the dev proxy). CI COUNTS are still fetched live at runtime. Run this script
 * when CI classes are added/removed.
 *
 * Usage:
 *   SN_USER=abeyahmad SN_PASSWORD='...' node scripts/refresh-class-tree.mjs
 *   # optional: SN_INSTANCE=https://dev380385.service-now.com (default below)
 *
 * Design notes:
 *  - The `cmdb_ci`-prefixed table set is the reliable backbone: it does NOT
 *    depend on extension-chain completeness (walking super_class from cmdb_ci
 *    drops whole subtrees when a non-admin user can't read an intermediate
 *    table's metadata row).
 *  - Any class that appears in the live counts but is missing from that backbone
 *    (non-prefixed CI subclasses like service_offering, or scoped-app classes)
 *    is attached so the rolled-up total always reconciles to the live total.
 *  - PARENT_OVERRIDES pins the nesting for classes whose metadata row isn't
 *    readable, so their counts roll up under the right parent (not just root).
 */

import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const INSTANCE = process.env.SN_INSTANCE || 'https://dev380385.service-now.com'
const USER = process.env.SN_USER
const PASSWORD = process.env.SN_PASSWORD

if (!USER || !PASSWORD) {
    console.error('error: set SN_USER and SN_PASSWORD environment variables')
    process.exit(1)
}

// Classes whose sys_db_object metadata row is not readable by this user (so
// their parent can't be discovered automatically). Pin known-correct parents.
const PARENT_OVERRIDES = {
    service_offering: 'cmdb_ci_service',
}

const auth = 'Basic ' + Buffer.from(`${USER}:${PASSWORD}`).toString('base64')

async function api(path) {
    const res = await fetch(`${INSTANCE}${path}`, {
        headers: { Accept: 'application/json', Authorization: auth },
    })
    if (!res.ok) throw new Error(`HTTP ${res.status} for ${path}`)
    return (await res.json()).result
}

// 1. Class shape for the cmdb_ci hierarchy (prefix-based backbone).
const classParams = new URLSearchParams({
    sysparm_query: 'nameSTARTSWITHcmdb_ci^ORname=cmdb_ci',
    sysparm_fields: 'name,label,super_class.name',
    sysparm_display_value: 'false',
    sysparm_limit: '10000',
})
console.error('Fetching class shape (sys_db_object dot-walk, ~30s)…')
const classes = await api(`/api/now/table/sys_db_object?${classParams}`)

// 2. Live leaf counts — source of truth for totals and orphan labels.
console.error('Fetching live CI counts…')
const buckets = await api(
    '/api/now/stats/cmdb_ci?sysparm_count=true&sysparm_group_by=sys_class_name&sysparm_display_value=all',
)
const own = {}
const ownLabel = {}
for (const b of Array.isArray(buckets) ? buckets : [buckets]) {
    const g = b.groupby_fields?.[0]
    if (!g) continue
    own[g.value] = Number(b.stats?.count || 0)
    ownLabel[g.value] = g.display_value || g.value
}

// Backbone nodes.
const nodes = new Map()
for (const c of classes) {
    nodes.set(c.name, {
        n: c.name,
        l: c.label || c.name,
        p: c.name === 'cmdb_ci' ? '' : c['super_class.name'] || '',
    })
}

// 3. Attach counted-but-missing classes so the total reconciles.
let attached = 0
for (const cls of Object.keys(own)) {
    if (nodes.has(cls)) continue
    nodes.set(cls, { n: cls, l: ownLabel[cls] || cls, p: PARENT_OVERRIDES[cls] || 'cmdb_ci' })
    attached++
}

const rows = [...nodes.values()].sort((a, b) => a.n.localeCompare(b.n))

// Reconcile check: rolled-up cmdb_ci total must equal the live total.
const children = {}
for (const r of rows) (children[r.p] ||= []).push(r.n)
const memo = {}
const rollup = (n) =>
    (memo[n] ??= (own[n] || 0) + (children[n] || []).reduce((s, c) => s + rollup(c), 0))
const liveTotal = Object.values(own).reduce((a, b) => a + b, 0)
const rootTotal = rollup('cmdb_ci')

const header = `// AUTO-GENERATED by scripts/refresh-class-tree.mjs from ${INSTANCE}.
// CMDB class schema (table name, label, parent table). The hierarchy changes
// rarely, so it is stored statically to avoid a slow (~30s) sys_db_object
// dot-walk at runtime; CI counts are still fetched live. Re-run the script to
// refresh. Each entry: n=table name, l=label, p=parent table.

export interface ClassSchemaRow {
    n: string
    l: string
    p: string
}

export const CLASS_SCHEMA: ClassSchemaRow[] = `

const __dirname = dirname(fileURLToPath(import.meta.url))
const outPath = join(__dirname, '..', 'src', 'client', 'data', 'classTree.ts')
writeFileSync(outPath, header + JSON.stringify(rows) + '\n')

console.error(`Wrote ${rows.length} classes (${attached} attached from live counts) -> ${outPath}`)
console.error(
    `Reconcile: cmdb_ci rolled-up=${rootTotal}  live total=${liveTotal}  ${rootTotal === liveTotal ? 'OK ✓' : 'MISMATCH ✗'}`,
)
if (rootTotal !== liveTotal) process.exit(2)
